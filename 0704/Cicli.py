# ciclo booleano

controllore = True
while controllore:
    print(1) # printerà 1 all'infinito
    controllore = False 

# ciclo while
conteggio = 0
while conteggio < 5:
    print(conteggio)
    conteggio += 1

# ciclo for
numeri = [1,2,3,4,5]

for numero in numeri: # numero è una variabile momentanea usata per scorrere tutti i numeri
    print(numero)
